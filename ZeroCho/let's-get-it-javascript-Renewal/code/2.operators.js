// Number
4 + 2; //6 (더하기)
4 - 2; //2 (빼기)
4 * 2; //8 (곱하기)
4 / 2; //2 (나누기)
4 % 2; //0 (나머지)
4 ** 2; //16 (제곱)

// string
"4" + "2"; //"42"
"유자" + "에이드"; //"유자에이드"
"유자" + " 에이드"; //"유자 에이드"
"유자" - " 에이드"; //"NaN"
"유자" * " 에이드"; //"NaN"
"유자" / " 에이드"; //"NaN"
"유자" % " 에이드"; //"NaN"

Infinity - Infinity //NaN
0 / 0 // NaN

// 형 변환
"문자열" + 0 // "문자열0"
"문자열" - 0 // NaN
1 + "0" // 1
"3" - 0 // 3